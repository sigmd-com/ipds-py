#!/bin/bash

# IPDS 기본 IP 조회 예제
# 이 스크립트는 IPDS의 기본적인 IP 조회 기능을 보여줍니다.

echo "=== IPDS 기본 IP 조회 예제 ==="
echo

echo "1. 단일 IP 주소 조회 (8.8.8.8)"
echo "----------------------------------------"
ipds 8.8.8.8
echo

echo "2. 프라이빗 IP 주소 조회 (192.168.1.1)"
echo "----------------------------------------"
ipds 192.168.1.1
echo

echo "3. IPv6 주소 조회 (2001:db8::1)"
echo "----------------------------------------"
ipds 2001:db8::1
echo

echo "4. 기본 정보만 조회 (8.8.8.8)"
echo "----------------------------------------"
ipds 8.8.8.8 --basic-only
echo

echo "5. ASN 정보만 조회 (8.8.8.8)"
echo "----------------------------------------"
ipds 8.8.8.8 --asn-only
echo

echo "6. 지리적 위치 정보만 조회 (8.8.8.8)"
echo "----------------------------------------"
ipds 8.8.8.8 --geo-only
echo

echo "7. 네트워크 정보만 조회 (8.8.8.8)"
echo "----------------------------------------"
ipds 8.8.8.8 --network-only
echo

echo "=== 예제 완료 ==="
